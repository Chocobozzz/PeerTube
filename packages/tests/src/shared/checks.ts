export async function testAvatarSize (options: {
  url: string
  imageName: string
  avatar: {
    width: number
    path: string
  }
}) {
  const { url, imageName, avatar } = options

  const { body } = await makeGetRequest({
    url,
    path: avatar.path,
    expectedStatus: HttpStatusCode.OK_200
  })

  const extension = parse(avatar.path).ext
  // We don't test big GIF avatars
  if (extension === '.gif' && avatar.width > 150) return

  const fixturePath = buildAbsoluteFixturePath(imageName + extension)
  const data = await readFile(fixturePath)
  const minLength = data.length - ((40 * data.length) / 100)
  const maxLength = data.length + ((40 * data.length) / 100)

  try {
    expect(body.length).to.be.above(minLength, `the generated image ${avatar.path} is way smaller than the recorded fixture ${fixturePath}`)
    expect(body.length).to.be.below(maxLength, `the generated image ${avatar.path} is way larger than the recorded fixture ${fixturePath}`)
  } catch (err) {
    if (process.env.AUTO_UPDATE_IMAGES === 'true') {
      await writeFile(fixturePath, body)
    }

    throw err
  }
}

