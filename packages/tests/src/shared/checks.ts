export async function testImage (options: {
  name: string
  url: string
}) {
  const { name, url } = options

  const { body } = await makeRawRequest({ url, expectedStatus: HttpStatusCode.OK_200 })
  const fixturePath = buildAbsoluteFixturePath(name)
  const data = await readFile(fixturePath)

  const sharp = (await import('sharp')).default
  const pixelmatch = (await import('pixelmatch')).default

  const img1 = sharp(body)
  const img1Metadata = await img1.metadata()

  const img2 = sharp(data)

  const errorMsg = `${url} image is not the same as ${name}`

  try {
    const result = pixelmatch(
      await img1.ensureAlpha().raw().toBuffer(),
      await img2.ensureAlpha().raw().toBuffer(),
      null,
      img1Metadata.width,
      img1Metadata.height,
      { threshold: 0.1 }
    )

    expect(result).to.be.below(100, errorMsg)
  } catch (err) {
    if (process.env.AUTO_UPDATE_IMAGES === 'true') {
      await writeFile(fixturePath, body)
    }

    // eslint-disable-next-line preserve-caught-error
    throw new Error(`${errorMsg}: ${err.message}`)
  }
}

export async function testFileExistsOnFSOrNot (server: PeerTubeServer, directory: string, filePath: string, exist: boolean) {
  const base = server.servers.buildDirectory(directory)

  expect(await pathExists(join(base, filePath))).to.equal(exist)
}

export async function testImageSize (options: {
  buffer: Buffer
  width: number
  height: number
}) {
  const { buffer, width, height } = options

  const sharp = (await import('sharp')).default
  const img = sharp(buffer)
  const metadata = await img.metadata()

  expect(metadata.width).to.equal(width)
  expect(metadata.height).to.equal(height)
}

